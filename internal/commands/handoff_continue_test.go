func TestHandoffContinue_MalformedBundleAsksForExplicitAgent(t *testing.T) {
	withClaudeOnPath(t, true)
	withStubExecutor(t)
	// Bundle present but missing the Original-session line.
	dir := t.TempDir()
	if resolved, err := filepath.EvalSymlinks(dir); err == nil {
		dir = resolved
	}
	cmd := exec.Command("git", "init", dir)
	cmd.Env = append(os.Environ(), "GIT_CONFIG_GLOBAL=/dev/null")
	_ = cmd.Run()
	semDir := filepath.Join(dir, ".semantica")
	_ = os.MkdirAll(semDir, 0o755)
	_ = os.WriteFile(filepath.Join(semDir, "handoff.md"), []byte("# header only\n"), 0o644)

	_, err := runRoot(t, []string{"--repo", dir, "handoff", "continue"})
	if err == nil {
		t.Fatal("expected error for unparseable bundle without --agent")
	}
	if !strings.Contains(err.Error(), "--agent") {
		t.Errorf("error should suggest --agent:\n%s", err.Error())
	}
}
